#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	n;

	n = ft_strlen(s);
	i = n;
	if (!s)
		return (NULL);
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i--;
	}
	return (NULL);
}
